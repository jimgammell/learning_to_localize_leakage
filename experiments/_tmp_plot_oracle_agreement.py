fig, axes = plt.subplots(3, 3, figsize=(15, 12))
axes = axes.flatten()

for idx, (reg_type, data) in enumerate(results.items()):
    ax = axes[idx]
    xs = sorted(data.keys())
    means = []
    for x in xs:
        if data[x] is not None:
            means.append(np.mean(data[x]))
        else:
            means.append(np.nan)

    ax.plot(xs, means, 'o-', color='C0', label='GradVis', zorder=3)
    ax.axhline(oracle_baseline_mean, color='green', linestyle='--', alpha=0.7, label='Oracle')
    ax.axhline(random_baseline_mean, color='red', linestyle='--', alpha=0.7, label='Random')
    ax.set_title(reg_type.replace('_', ' ').title())
    ax.set_xlabel('Regularization strength')
    ax.set_ylabel('Mean oracle agreement')
    ax.legend(fontsize=7)
    ax.grid(True, alpha=0.3)

    # Use log scale for weight_decay and gaussian_noise
    if reg_type in ('weight_decay', 'gaussian_noise'):
        positive_xs = [x for x in xs if x > 0]
        positive_means = [m for x, m in zip(xs, means) if x > 0]
        ax.clear()
        if any(x == 0 for x in xs):
            zero_mean = means[xs.index(0)]
            ax.axhline(zero_mean, color='C0', linestyle=':', alpha=0.5, label=f'No reg ({zero_mean:.3f})')
        ax.plot(positive_xs, positive_means, 'o-', color='C0', label='GradVis', zorder=3)
        ax.axhline(oracle_baseline_mean, color='green', linestyle='--', alpha=0.7, label='Oracle')
        ax.axhline(random_baseline_mean, color='red', linestyle='--', alpha=0.7, label='Random')
        ax.set_xscale('log')
        ax.set_title(reg_type.replace('_', ' ').title())
